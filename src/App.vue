<template>
  <h1>Reactor</h1>
  <button @click="start" :disabled="isPlaying">start</button>
  <Block v-if="isPlaying" :delay="delay" />
  <Results />
</template>

<script>
import Block from "./components/Block.vue";
import Results from "./components/Results.vue";

export default {
  name: "App",
  components: {
    Block,
    Results,
  },
  data() {
    return {
      delay: null,
      isPlaying: false,
    };
  },
  methods: {
    start() {
      this.delay = 1000 + Math.random() * 6000;
      this.isPlaying = true;
    },
  },
};
</script>

